import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
REAL_IMAGE_MANIFEST = ROOT / "data" / "real_images.json"
EXTRA_SCENES_PATH = ROOT / "data" / "extra_scenes.json"


def load_real_images():
    if not REAL_IMAGE_MANIFEST.exists():
        return {}
    return json.loads(REAL_IMAGE_MANIFEST.read_text(encoding="utf-8"))


def load_extra_scenes():
    if not EXTRA_SCENES_PATH.exists():
        return []
    return json.loads(EXTRA_SCENES_PATH.read_text(encoding="utf-8"))


REAL_IMAGES = load_real_images()

WIKIDATA = {
    "name": "Wikidata",
    "url": "https://www.wikidata.org/wiki/Wikidata:Data_access",
    "fields": ["title", "year", "director"],
}

REAL_IMAGE_SOURCE = {
    "name": "Wikipedia/Wikimedia page image",
    "url": "https://www.wikipedia.org/",
    "fields": ["image"],
}

MANUAL_CURATION = {
    "name": "Manual prototype curation",
    "url": "/readme.md",
    "fields": ["mood", "style", "story", "palette", "data_tags", "prompt"],
}

NOTE = (
    "Title, year, and director are grounded in public film metadata. "
    "Mood/style/story tags are manually curated for this prototype and should be "
    "replaced or weighted with MovieNet, LIRIS-ACCEDE, MovieGraphs, MovieLens, or MPST labels later."
)


def asset(scene_id):
    return REAL_IMAGES.get(scene_id, {}).get("local_path")


def image_status(scene_id):
    return REAL_IMAGES.get(scene_id, {}).get("image_status", "missing")


def image_note(scene_id):
    return REAL_IMAGES.get(scene_id, {}).get("usage_note", "No real image found yet.")


def image_page(scene_id):
    return REAL_IMAGES.get(scene_id, {}).get("page_url")


def sources():
    return [WIKIDATA, REAL_IMAGE_SOURCE, MANUAL_CURATION]


